        //  we can add backend validate here using laravel vaidator


        $order=new Order();
        $order->company=$request->input("company-name");
        $order->price=$request->input("price");
        $order->stoploss=$request->input("stoploss");
        $order->target=$request->input("target");
        $order->category=$request->input("category");
        $order->type=$request->input("type");
        $order->save();

        return redirect('task');


